    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
            boolean flag=Validator();
            User user = new User();
            if(flag==true)
            {
            user.Name = jTextField1.getText();
            user.UserId = jTextField2.getText();
            char[] pass = jPasswordField1.getPassword();
            String pwd = new String(pass);
            user.Password=pwd;
            user.commandname="Register";
              try
              {
                  Socket socket = new Socket(ChallengeClient.IpAdress,ChallengeClient.PortNo);

                ObjectOutputStream oos = new ObjectOutputStream(socket.getOutputStream() );
                oos.writeObject(user);
             }
              catch(Exception e)
                {
         
                    System.out.println(e);  
                }
            }
             JOptionPane.showMessageDialog(null, "Registration done.");
    }//GEN-LAST:event_jButton1ActionPerformed

