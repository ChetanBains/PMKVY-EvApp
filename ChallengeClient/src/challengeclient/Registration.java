    private boolean Validator()
    {
        
        if(jTextField1.getText().length()==0)
        {
            JOptionPane.showMessageDialog(this, "name is empty");
            return false;
        }
       
        if(jTextField2.getText().length()==0)
        {
            JOptionPane.showMessageDialog(this, "User Id field is empty");
            return false;
        }
          char[] pass = jPasswordField1.getPassword();
            String pwd = new String(pass);
            char[] pass1 = jPasswordField2.getPassword();
            String pwd1 = new String(pass1);
        if(pwd.length()==0&& !pwd.equals(pwd1))
        {
            JOptionPane.showMessageDialog(this, "enter correct password");
            return false;
        }
        return true;
      
    }
   
