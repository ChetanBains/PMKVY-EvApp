    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Boolean flag=ValidateInput();
        if(flag.equals(true))
        {
            components.User user = new components.User();
            user.UserNo = ChallengeClient.LoggedUser.UserNo;
            user.Password=jPasswordField2.getText();
            user.commandname = "ChangePassword";
            
            try {
                Socket socket = new Socket(ChallengeClient.IpAdress,ChallengeClient.PortNo);
                ObjectOutputStream os = new ObjectOutputStream(socket.getOutputStream());
                os.writeObject(user);
                JOptionPane.showMessageDialog(this, "password changed",ChallengeClient.Title, JOptionPane.INFORMATION_MESSAGE);
                
            } catch (Exception e) {
                System.out.println(e);
            }
            
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

   private Boolean ValidateInput()
   {
       if(!ChallengeClient.LoggedUser.Password.equals(jPasswordField1.getText()))
       {
           JOptionPane.showMessageDialog(this, "Old password Incorrect.",ChallengeClient.Title, JOptionPane.WARNING_MESSAGE);
           return false;
       }
       if(!jPasswordField2.getText().equals(jPasswordField3.getText()))
       {
            JOptionPane.showMessageDialog(this, "Password mismatch.",ChallengeClient.Title, JOptionPane.WARNING_MESSAGE);
           return false;
       }
       return true;
   }

