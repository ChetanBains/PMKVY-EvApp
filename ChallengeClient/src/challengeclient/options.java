    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed

        ChallengeClient.LoggedUser.commandname = "SignOut";
        try {
            Socket socket = new Socket(ChallengeClient.IpAdress, ChallengeClient.PortNo);
            ObjectOutputStream obj = new ObjectOutputStream(socket.getOutputStream());
            obj.writeObject(ChallengeClient.LoggedUser);
            ChallengeClient.LoggedUser.UserNo = 0;
            this.dispose();
                    
        } catch (Exception e) {
            System.out.println(e);
        }
        // JOptionPane.showMessageDialog(null, "logged out!",ChallengeClient.Title, JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_jButton3ActionPerformed

